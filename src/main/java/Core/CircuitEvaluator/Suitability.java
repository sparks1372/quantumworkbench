package Core.CircuitEvaluator;

/**
 * @author Sam Ratcliff
 */
public class Suitability {

	/**
	 * @uml.property name="fitness" readOnly="true"
	 */
	private final double	fitness;

	/**
	 * @uml.property name="perfectcount" readOnly="true"
	 */
	private final int		perfectcount;

	public Suitability(double fitness, int perfectcount) {
		super();
		this.fitness = fitness;
		this.perfectcount = perfectcount;
	}

	/**
	 * Getter of the property <tt>fitness</tt>
	 * 
	 * @return Returns the fitness.
	 * @uml.property name="fitness"
	 */
	public double getFitness() {
		return fitness;
	}

	/**
	 * Getter of the property <tt>perfectcount</tt>
	 * 
	 * @return Returns the perfectcount.
	 * @uml.property name="perfectcount"
	 */
	public int getPerfectcount() {
		return perfectcount;
	}

}
